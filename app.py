import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
 
# ======================
# Konfigurasi halaman
# ======================
st.set_page_config(page_title="Dashboard Penjualan", layout="wide")

st.title("Dashboard Analisis Penjualan")
st.markdown("Analisis tren penjualan dan top produk (Jan–Agustus 2018)")

# ======================
# Load Data
# ======================
@st.cache_data
def load_data():
    df = pd.read_csv("df_analysis.csv")
    df['order_purchase_timestamp'] = pd.to_datetime(df['order_purchase_timestamp'])
    return df

df = load_data()

# ======================
# Filter Data (Jan–Agustus 2018)
# ======================
df = df[
    (df['order_purchase_timestamp'] >= '2018-01-01') &
    (df['order_purchase_timestamp'] <= '2018-08-31')
]

# ======================
# KPI
# ======================
total_revenue = df['revenue'].sum()
total_orders = df['order_id'].nunique()

col1, col2 = st.columns(2)
col1.metric("Total Revenue", f"{total_revenue:,.0f}")
col2.metric("Total Orders", total_orders)

# ======================
# Pertanyaan 1: Tren Bulanan
# ======================
st.subheader("Tren Revenue Bulanan (MoM)")

df['year_month'] = df['order_purchase_timestamp'].dt.to_period('M')

monthly_sales = df.groupby('year_month').agg({
    'order_id': 'nunique',
    'revenue': 'sum'
}).reset_index()

monthly_sales['MoM_growth_%'] = monthly_sales['revenue'].pct_change() * 100

# Identifikasi penurunan terbesar
drop_month = monthly_sales.loc[monthly_sales['MoM_growth_%'].idxmin()]

st.write(f"Penurunan terbesar terjadi pada: **{drop_month['year_month']}**")

# Visualisasi
fig1, ax1 = plt.subplots()
ax1.plot(monthly_sales['year_month'].astype(str), monthly_sales['revenue'])
ax1.set_title("Revenue Bulanan")
plt.xticks(rotation=45)
st.pyplot(fig1)

# ======================
# Pertanyaan 2: Top Produk
# ======================
st.subheader("Top 10 Produk Berdasarkan Revenue")

product_sales = df.groupby('product_id')['revenue'].sum().sort_values(ascending=False)

top10 = product_sales.head(10).reset_index()

total_rev = product_sales.sum()
top10['contribution_%'] = (top10['revenue'] / total_rev) * 100

# Visualisasi
fig2, ax2 = plt.subplots()
ax2.barh(top10['product_id'].astype(str), top10['revenue'])
ax2.invert_yaxis()
ax2.set_title("Top 10 Produk")
st.pyplot(fig2)
 
# Tabel kontribusi
st.dataframe(top10)
